        "import os\n",
        "import pandas as pd\n",
        "import numpy as np\n",
        "from astropy.io import fits\n",
        "from datetime import datetime, timedelta\n",
        "\n",
        "def timestamp2datetime(time_arr, ref_date=datetime(2023, 1, 1)):\n",
        "    return [ref_date + timedelta(seconds=float(t)) for t in time_arr]\n",
        "\n",
        "def load_solexs(file_path):\n",
        "    with fits.open(file_path) as hdul:\n",
        "        data = hdul[1].data\n",
        "        time_col = data['TIME']\n",
        "        counts_col = data['COUNTS']\n",
        "        # TIME is Unix timestamp (seconds since 1970-01-01)\n",
        "        times = pd.to_datetime(np.array(time_col, dtype=np.float64), unit='s', utc=True).tz_localize(None)\n",
        "        counts_np = np.array(counts_col, dtype=np.float64)\n",
        "        df = pd.DataFrame({'time': times, 'soft_flux': counts_np})\n",
        "    return df\n",

        "\n",
        "def load_hel1os(file_path):\n",
        "    # HEL1OS uses MJD for time and CTR for count rate (not TIME/COUNTS)\n",
        "    # Extension 5 = broadband 1.8-90 keV (best for flare detection)\n",
        "    from astropy.time import Time\n",
        "    with fits.open(file_path) as hdul:\n",
        "        ext_idx = 5 if len(hdul) > 5 else 1\n",
        "        data = hdul[ext_idx].data\n",
        "        mjd_col = data['MJD']\n",
        "        ctr_col = data['CTR']\n",
        "        times = Time(mjd_col, format='mjd').to_datetime()\n",
        "        ctr_np = np.array(ctr_col, dtype=np.float64)\n",
        "        df = pd.DataFrame({'time': times, 'hard_flux': ctr_np})\n",
        "    return df\n",
        "\n",
        "print('Loading entire dataset directory...')\n",
        "data_dir = '.'\n",
        "solexs_files = sorted([f for f in os.listdir(data_dir) if f.endswith('.lc.gz')])\n",
        "hel1os_files = sorted([f for f in os.listdir(data_dir) if f.endswith('.fits') and 'lightcurve' in f])\n",
        "\n",
        "print(f'Found {len(solexs_files)} SoLEXS files and {len(hel1os_files)} HEL1OS files')\n",
        "\n",
        "solexs_dfs, hel1os_dfs = [], []\n",
        "for f in solexs_files:\n",
        "    try:\n",
        "        solexs_dfs.append(load_solexs(os.path.join(data_dir, f)))\n",
        "    except Exception as e:\n",
        "        print(f'Skipping {f}: {e}')\n",
        "\n",
        "for f in hel1os_files:\n",
        "    try:\n",
        "        hel1os_dfs.append(load_hel1os(os.path.join(data_dir, f)))\n",
        "    except Exception as e:\n",
        "        print(f'Skipping {f}: {e}')\n",
        "\n",
        "if not solexs_dfs or not hel1os_dfs:\n",
        "    print('WARNING: Missing files. Please upload both SoLEXS and HEL1OS files!')\n",
        "else:\n",
        "    all_solexs = pd.concat(solexs_dfs).sort_values('time').reset_index(drop=True)\n",
        "    all_hel1os = pd.concat(hel1os_dfs).sort_values('time').reset_index(drop=True)\n",
        "\n",
        "    print('Synchronizing timelines...')\n",
        "    fused_df = pd.merge_asof(all_solexs, all_hel1os, on='time', direction='nearest', tolerance=pd.Timedelta('2s'))\n",
        "    fused_df = fused_df.set_index('time').resample('1s').mean().ffill().dropna().reset_index()\n",
        "\n",
        "    print('Extracting physical features...')\n",
        "    fused_df['flux_ratio'] = fused_df['hard_flux'] / (fused_df['soft_flux'] + 1e-10)\n",
        "    fused_df['soft_rise'] = fused_df['soft_flux'].diff().fillna(0)\n",
        "    fused_df['hard_spike'] = fused_df['hard_flux'].diff().fillna(0)\n",
        "\n",
        "    threshold = fused_df['hard_spike'].quantile(0.95)\n",
        "    fused_df['is_flare'] = (fused_df['hard_spike'] > threshold).astype(int)\n",
        "    fused_df['flare_label'] = fused_df['is_flare'].shift(-900).fillna(0)\n",
        "\n",
        "    # Generating Master Flare Catalogue\n",
        "    flare_events = fused_df[fused_df['is_flare'] == 1][['time', 'soft_flux', 'hard_flux']].copy()\n",
        "    flare_events.to_csv('flare_catalogue.csv', index=False)\n",
        "    print(f'Saved Master Flare Catalogue with {len(flare_events)} events to flare_catalogue.csv')\n",
        "\n",
        "    print(f'Data fusion complete! Total rows: {fused_df.shape[0]}')\n"
