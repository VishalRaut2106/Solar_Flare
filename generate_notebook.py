        "import json\n",
        "import torch\n",

        "import torch.nn as nn\n",
        "from torch.utils.data import Dataset, DataLoader\n",
        "from sklearn.preprocessing import MinMaxScaler\n",
        "\n",
        "class FlareDataset(Dataset):\n",
        "    def __init__(self, df, seq_length=60):\n",
        "        features = ['soft_flux', 'hard_flux', 'flux_ratio', 'soft_rise', 'hard_spike']\n",
        "        df = df.replace([np.inf, -np.inf], np.nan).fillna(0)\n",
        "        self.scaler = MinMaxScaler()\n",
        "        data = self.scaler.fit_transform(df[features].values)\n",
        "        X, y = [], []\n",
        "        if len(data) > seq_length:\n",
        "            for i in range(0, len(data) - seq_length, 10):\n",
        "                X.append(data[i:i+seq_length])\n",
        "                y.append(df.iloc[i+seq_length]['flare_label'])\n",
        "        self.X = torch.tensor(np.array(X), dtype=torch.float32)\n",
        "        self.y = torch.tensor(np.array(y), dtype=torch.long)\n",
        "\n",
        "    def __len__(self): return len(self.X)\n",
        "    def __getitem__(self, idx): return self.X[idx], self.y[idx]\n",
        "\n",
        "class FlareLSTM(nn.Module):\n",
        "    def __init__(self):\n",
        "        super().__init__()\n",
        "        self.lstm = nn.LSTM(5, 64, 2, batch_first=True)\n",
        "        self.fc = nn.Linear(64, 2)\n",
        "\n",
        "    def forward(self, x):\n",
        "        lstm_out, _ = self.lstm(x)\n",
        "        return self.fc(lstm_out[:, -1, :])\n",
        "\n",
        "if 'fused_df' in dir():\n",
        "    print('Preparing tensors...')\n",
        "    dataset = FlareDataset(fused_df)\n",
        "    loader = DataLoader(dataset, batch_size=256, shuffle=True)\n",
        "\n",
        "    model = FlareLSTM()\n",
        "    criterion = nn.CrossEntropyLoss()\n",
        "    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)\n",
        "\n",
        "    print('Training Deep Learning Model on 10-day dataset...')\n",
        "    model.train()\n",
        "    for epoch in range(5):\n",
        "        epoch_loss = 0.0\n",
        "        for X_batch, y_batch in loader:\n",
        "            optimizer.zero_grad()\n",
        "            output = model(X_batch)\n",
        "            loss = criterion(output, y_batch)\n",
        "            loss.backward()\n",
        "            optimizer.step()\n",
        "            epoch_loss += loss.item()\n",
        "        print(f'Epoch {epoch+1}, Loss: {epoch_loss/len(loader):.4f}')\n",
        "    print('Training Complete!')\n",
        "\n",
        "    model.eval()\n",
        "    with torch.no_grad():\n",
        "        last_sequence = dataset.X[-1].unsqueeze(0)\n",
        "        probs = torch.softmax(model(last_sequence), dim=1)\n",
        "        flare_prob = probs[0][1].item()\n",
        "\n",
        "    alert_data = {\n",
        "        'timestamp': str(fused_df['time'].iloc[-1]),\n",
        "        'flare_probability': flare_prob,\n",
        "        'alert_level': 'HIGH' if flare_prob > 0.7 else ('MEDIUM' if flare_prob > 0.4 else 'LOW'),\n",
        "        'lead_time_mins': 15,\n",
        "        'method': 'PyTorch LSTM Forecast (10-Day Authentic Fused)'\n",
        "    }\n",
